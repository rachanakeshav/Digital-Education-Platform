    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tablePersonList.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int dailogBox = JOptionPane.YES_NO_OPTION;
            int dailogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this faculty?", "Warning", dailogBox);
            if (dailogResult == JOptionPane.YES_OPTION) {
                DefaultTableModel model = (DefaultTableModel) tablePersonList.getModel();
                Faculty faculty = (Faculty) model.getValueAt(selectedRowIndex, 0);
                facultyDirectory.removeFaculty(faculty);
                JOptionPane.showMessageDialog(this, "faculty has been deleted.");
                populateTable();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

