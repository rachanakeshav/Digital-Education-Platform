    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tablePersonList.getModel();
        model.setRowCount(0);
        for (Faculty faculty : facultyDirectory.getFaculties()) {
            Object[] row = new Object[4];
            row[0] = faculty;
            row[1] = faculty.getName();
            row[2] = faculty.getEmail();
            row[3] = faculty.getDepartment();

            model.addRow(row);
        }
    }
