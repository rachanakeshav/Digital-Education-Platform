    private void btnView1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnView1ActionPerformed
        int selectedRowIndex = tblAssignment.getSelectedRow();

        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblAssignment.getModel();
        Object vitalSigns = (Assignment) model.getValueAt(selectedRowIndex, 0);

        if(vitalSigns instanceof Assignment a){
            txtName.setText(a.getName());
            txtGithub.setText(a.getLink());
            txtDueDate.setText(a.getDueDate());
            txtDesc.setText(a.getDescription());
        }
        else{
            JOptionPane.showMessageDialog(this, "Invalid data in the selected row.");
        }
    }//GEN-LAST:event_btnView1ActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        int selectedRowIndex = tblAssignment.getSelectedRow();

        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select a row to delete.");
            return;
        }
        
        if(txtGithub.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Github Link");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblAssignment.getModel();
        Assignment a = (Assignment) model.getValueAt(selectedRowIndex, 0);  
        if(a.isUploaded()){
            JOptionPane.showMessageDialog(null, "Already Submitted", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        assignmentDirectory.uploadAssignment(a.getID());
        users.refreshUserList();
        JOptionPane.showMessageDialog(this, "Assignment Uploaded");
        PopulateTable();
        ResetFields();
    }//GEN-LAST:event_btnSubmitActionPerformed


