    public void PopulateTable(){
        DefaultTableModel tbl = (DefaultTableModel) tblAssignment.getModel();
        tbl.setRowCount(0);
        var u = users.getUserById(user.getId());
        ArrayList<Assignment> assignments = utils.getAssignmentsWithStudentID(user.getId());
        for(Assignment a : assignments){
            Object[] row = new Object[5];
            row[0] = a;
            row[1] = a.getName();
            row[2] = a.getDueDate();
            row[3] = a.getPoints();
            row[4] = a.isUploaded();
            tbl.addRow(row); 
        }
    }
    
